class Solution(object):
    def depthSum(self, nestedList):
        """
        :type nestedList: List[NestedInteger]
        :rtype: int
        """
        if not nestedList: return 0
        self.res = 0
        level = 1 
        self.dfs(nestedList, level)
        return self.res
    
    def dfs(self,nests, level):
        if not nests:
            return
        for nest in nests:
            if nest.isInteger():
                value = nest.getInteger()
                self.res += level * value
            else:
                self.dfs(nest.getList(), level+1)
                    
                
